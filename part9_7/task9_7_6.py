# –ê —Å—Ç–æ–∏–º–æ—Å—Ç—å –≤—Å–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è —Å–∫–ª–∞–¥—ã–≤–∞–µ—Ç—Å—è –∏–∑ —Å—É–º–º—ã —Å—Ç–æ–∏–º–æ—Å—Ç–µ–π –≤—Å–µ—Ö —Å–∏–º–≤–æ–ª–æ–≤.
# –°—ç–º—É –∑–∞—Ö–æ—Ç–µ–ª–æ—Å—å –ø–æ–¥—Å—á–∏—Ç–∞—Ç—å, —Å–∫–æ–ª—å–∫–æ üêù –æ–Ω –∑–∞—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç –∑–∞ —Å–≤–æ–∏ –æ—Ç–≤–µ—Ç—ã –≤ –∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏—è—Ö, –∏ –ø—Ä–æ—Å–∏—Ç –≤–∞—Å –ø–æ–º–æ—á—å –µ–º—É.
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—ë—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞.
# –ù–∞ —ç—Ç–æ—Ç —Ä–∞–∑ –°—ç–º—É –∑–∞—Ö–æ—Ç–µ–ª–æ—Å—å —Å—Ö–∏—Ç—Ä–∏—Ç—å –∏ –ø–æ–ø—Ä–æ–±–æ–≤–∞—Ç—å —É–≤–µ–ª–∏—á–∏—Ç—å —Å—Ç–æ–∏–º–æ—Å—Ç—å —Å–≤–æ–µ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è, –∑–∞–º–µ–Ω–∏–≤ –≤ –Ω–µ–º –Ω–µ–∫–æ—Ç–æ—Ä—ã–µ
# –±—É–∫–≤—ã: eyopaxcETOPAHXCBM –Ω–∞ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–µ –∏–º —Ä—É—Å—Å–∫–∏–µ –±—É–∫–≤—ã –µ—É–æ—Ä–∞—Ö—Å–ï–¢–û–†–ê–ù–•–°–í–ú.
# –ù–∞ –≤—Ö–æ–¥ –ø—Ä–æ–≥—Ä–∞–º–º–µ –ø–æ–¥–∞—ë—Ç—Å—è —Å—Ç—Ä–æ–∫–∞ —Ç–µ–∫—Å—Ç–∞. –¢—Ä–µ–±—É–µ—Ç—Å—è –Ω–∞–ø–∏—Å–∞—Ç—å –ø—Ä–æ–≥—Ä–∞–º–º—É, –∫–æ—Ç–æ—Ä–∞—è –Ω–∞—Ö–æ–¥–∏—Ç —Å—Ç–æ–∏–º–æ—Å—Ç—å —Å—Ç–∞—Ä–æ–≥–æ –∏ –Ω–æ–≤–æ–≥–æ
# —Å–æ–æ–±—â–µ–Ω–∏–π –°—ç–º–∞ –≤ üêù –∏ –≤—ã–≤–æ–¥–∏—Ç —Ç–µ–∫—Å—Ç –≤ —Å–ª–µ–¥—É—é—â–µ–º —Ñ–æ—Ä–º–∞—Ç–µ:
# –°—Ç–∞—Ä–∞—è —Å—Ç–æ–∏–º–æ—Å—Ç—å: <—Å—Ç–æ–∏–º–æ—Å—Ç—å —Å—Ç–∞—Ä–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è>üêù
# –ù–æ–≤–∞—è —Å—Ç–æ–∏–º–æ—Å—Ç—å: <—Å—Ç–æ–∏–º–æ—Å—Ç—å –Ω–æ–≤–æ–≥–æ —Å–æ–æ–±—â–µ–Ω–∏—è>üêù


message = input()
eng_letters = 'eyopaxcETOPAHXCBM'
rus_letters = '–µ—É–æ—Ä–∞—Ö—Å–ï–¢–û–†–ê–ù–•–°–í–ú'
old_message = message
old_cost = sum(ord(char) for char in old_message) * 3
new_message = ''
for char in message:
    if char in eng_letters:
        index = eng_letters.find(char)
        new_message += rus_letters[index]
    else:
        new_message += char
new_cost = sum(ord(char) for char in new_message) * 3

print(f'–°—Ç–∞—Ä–∞—è —Å—Ç–æ–∏–º–æ—Å—Ç—å: {old_cost}üêù')
print(f'–ù–æ–≤–∞—è —Å—Ç–æ–∏–º–æ—Å—Ç—å: {new_cost}üêù')